package scanner;

public class SwitchCase {

	public static void main(String[] args) {
		
		switch(2)
		{
		case 1:System.out.println("Print1");
		break;
		case 2:System.out.println("Print2");
		break;
		case 3:System.out.println("Print3");
		break;
		case 4:System.out.println("Print4");
		break;
		case 5:System.out.println("Print5");
		break;
		
		}

	}

}


