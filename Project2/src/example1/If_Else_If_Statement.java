public class If_Else_If_Statement {

	public static void main(String[] args) 
	{
		int a=18;
		if(a==17)
		{
			System.out.println("1");
		}
		else if(a>100)
		{
			System.out.println("2");
		}
		else if(a>20)
		{
			System.out.println("4");
		}
		else
		{
			System.out.println("3");
		}
		

	}

}
